def metade(valor):
    x = (valor/2)
    return f"a metade de {valor} é {x} "


def dobro(valor):
    x = (valor*2)
    return f"O dobro de {valor} é {x}"


def aumentar(valor):
    x = ((valor*(10/100)) + valor)
    return f"{valor} aumentado 10% é {x}"


def reduzindo(valor):
    x = (valor - (valor*(13/100)))
    return f"{valor} reduzido 13% é {x}"
